#!/usr/local/bin/python3

# https://www.tutorialsteacher.com/python/python-comparison-and-logical-operators

# Comparisons
a = 1
b = 2

print(a < b)
print(a > b)
print(a == b)
print(a != b)
print(a >= b)
print(a <= b)

# Logical operations
print(a and b)
print(a or b)
print(not b)

# Making decisions
if a:
    print('a is True')
else:
    print('a is False')

# elif - else if can be stacked to check multiple conditions
